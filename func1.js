
//While Loop
var i=1;
while(i<=5){
    console.log(i)
    i=i+1;
}
console.log('Counting completed!')

var i=5;
while(i>=0){
    console.log(i)
    i=i-1;
}
console.log('Countdown finished!')

//For Loop
for(var i=5; i>0; i--){
    console.log(i)
}
console.log("Countdown finished!")


for(var i=1; i<=5; i++){
    console.log(i)
}
console.log("Counting completed!")

